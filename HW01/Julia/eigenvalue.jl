function inverse_iter(K,M,x,tol)
	# K → stiffness matrix
	# M → mass matrix
	# x → initial guess
	# tol → tolerence

	y = M*x

	err = tol * 2 # any number above the tolerence
	ρₙ₁ = 0.0
	ρₙ = 0.0
	n = 0 # counter to count number of loops before convergence

	while err ≥ tol
		n +=1
		xbar = inv(K) * y
		ybar = M*xbar
		ρₙ = ρₙ₁
		ρₙ₁ = (xbar'*y)/(xbar'*ybar)
		err = abs(ρₙ₁ - ρₙ )/ ρₙ₁
		y = ybar/sqrt(xbar'*ybar)
	 end

	λ = ρₙ₁
	ϕ = inv(M) * y
	ϕ = ϕ/norm(ϕ)

	eigen = Eigen(λ,ϕ)
	num_iter = n
	(;eigen,num_iter)

end

