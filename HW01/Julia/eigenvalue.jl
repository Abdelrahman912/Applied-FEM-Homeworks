	ϕ::Eigenvector
end

# ╔═╡ b8d722d3-3074-4888-b0a8-4ea99d8f9e00
function forward_iter(K,M,x,tol)
	# K → stiffness matrix
	# M → mass matrix
	# x → initial guess
	# tol → tolerence

	y = K*x

	err = tol * 2 # any number above the tolerence
	ρₙ₁ = 0.0
	ρₙ = 0.0
	n = 0 # counter to count number of loops before convergence
	
	while err ≥ tol
		n +=1
		xbar = inv(M) * y
		ybar = K*xbar
		ρₙ = ρₙ₁
		ρₙ₁ = (xbar'*ybar)/(xbar'*y)
		err = abs(ρₙ₁ - ρₙ )/ ρₙ₁
		y = ybar/sqrt(xbar'*y)
	 end

	λ = ρₙ₁
	ϕ = inv(K) * y
	ϕ = ϕ/norm(ϕ)

	eigen = Eigen(λ,ϕ)
	num_iter = n
	(;eigen,num_iter)
end

